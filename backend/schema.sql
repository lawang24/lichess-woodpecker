CREATE TABLE IF NOT EXISTS puzzle_completions (
    cycle_id BIGINT NOT NULL,
    set_id BIGINT NOT NULL,
    puzzle_id TEXT NOT NULL,
    completed_at DOUBLE PRECISION NOT NULL,
    solved BOOLEAN NOT NULL DEFAULT FALSE,
    PRIMARY KEY (cycle_id, puzzle_id)
);

DO $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'cycle_completions_pkey'
          AND conrelid = 'puzzle_completions'::regclass
    )
    AND NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'puzzle_completions_pkey'
          AND conrelid = 'puzzle_completions'::regclass
    ) THEN
        ALTER TABLE puzzle_completions
            RENAME CONSTRAINT cycle_completions_pkey TO puzzle_completions_pkey;
    END IF;
END
$$;

ALTER TABLE puzzle_completions
    ADD COLUMN IF NOT EXISTS set_id BIGINT;

UPDATE puzzle_completions pc
SET set_id = c.set_id
FROM cycles c
WHERE pc.cycle_id = c.id
  AND pc.set_id IS NULL;

ALTER TABLE puzzle_completions
    ALTER COLUMN set_id SET NOT NULL;

ALTER TABLE puzzle_completions
    ADD COLUMN IF NOT EXISTS solved BOOLEAN NOT NULL DEFAULT FALSE;

UPDATE puzzle_completions
SET solved = FALSE
WHERE solved IS NULL;

ALTER TABLE puzzle_completions
    ALTER COLUMN solved SET DEFAULT FALSE,
    ALTER COLUMN solved SET NOT NULL;

DO $$
BEGIN
    IF NOT EXISTS (
