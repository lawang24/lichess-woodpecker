            FOREIGN KEY (set_id, puzzle_id)
            REFERENCES puzzle_set_items (set_id, puzzle_id)
            ON DELETE CASCADE;
    END IF;
END
$$;

CREATE INDEX IF NOT EXISTS puzzle_completions_cycle_idx
